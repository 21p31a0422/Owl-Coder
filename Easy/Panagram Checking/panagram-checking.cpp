class Solution
{
  public:
    //Function to check if a string is Pangram or not.
    bool checkPangram (string s) {
        // your code here
        int a = 0;
        for (auto it : s){
            if (isalpha(it)) a = (a | 1 << (tolower(it) - 'a'));
        } if (a == ((1 << 26) - 1)) return true;
        return false;
    }

};

